using System.Collections.Immutable;
using System.Text;
using Microsoft.CodeAnalysis;
using Microsoft.CodeAnalysis.Text;

namespace Macaron.FluentEnum;

internal static class SourceGenerationHelper
{
    private const string Indent = "    ";

    public static void AddSource(
        SourceProductionContext context,
        ExtensionClassModel extensionClassModel,
        string hintName,
        ImmutableArray<ExtensionMethodModel> methodModels
    )
    {
        var lines = ExtensionMethodRenderer.Render(methodModels, Indent);

        if (lines.IsDefaultOrEmpty)
        {
            return;
        }

        var stringBuilder = new StringBuilder();

        stringBuilder.AppendLine("// <auto-generated />");
        stringBuilder.AppendLine("#nullable enable");
        stringBuilder.AppendLine();

        var hasNamespace = extensionClassModel.Namespace.Length > 0;

        // namespace begin
        if (hasNamespace)
        {
            stringBuilder.AppendLine($"namespace {extensionClassModel.Namespace}");
            stringBuilder.AppendLine("{");
        }

        var depthSpacerText = hasNamespace ? Indent : "";

        // type declaration begin
        stringBuilder.AppendLine(
            $"{depthSpacerText}{extensionClassModel.AccessModifier} static partial class {extensionClassModel.ClassName}"
        );
        stringBuilder.AppendLine($"{depthSpacerText}{{");

        depthSpacerText += Indent;

        foreach (var line in lines)
        {
            stringBuilder.AppendLine($"{(line.Length > 0 ? depthSpacerText : "")}{line}");
        }

        // type declaration end
        depthSpacerText = depthSpacerText[..^Indent.Length];
        stringBuilder.AppendLine($"{depthSpacerText}}}");

        // namespace end
        if (hasNamespace)
        {
            stringBuilder.AppendLine("}");
        }

        context.AddSource(
            hintName: hintName,
            sourceText: SourceText.From(stringBuilder.ToString(), Encoding.UTF8)
        );
    }
}
